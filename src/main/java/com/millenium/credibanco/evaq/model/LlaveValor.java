package com.millenium.credibanco.evaq.model;



public class LlaveValor {

	private String llave;
	private String valor;
	private String riesgo;
	private String detalleError;
	private Double ponderacion;
	private String valorPonderado;


	public LlaveValor() {

	}

	
	public LlaveValor(String llave, String valor, String riesgo) {
		super();
		this.llave = llave;
		this.valor = valor;
		this.riesgo = riesgo;
	}

	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}


	public String getLlave() {
		return llave;
	}


	public void setLlave(String llave) {
		this.llave = llave;
	}


	public String getRiesgo() {
		return riesgo;
	}
	public void setRiesgo(String riesgo) {
		this.riesgo = riesgo;
	}


	public String getDetalleError() {
		return detalleError;
	}


	public void setDetalleError(String detalleError) {
		this.detalleError = detalleError;
	}


	public Double getPonderacion() {
		return ponderacion;
	}
	public void setPonderacion(Double ponderacion) {
		this.ponderacion = ponderacion;
	}


	public String getValorPonderado() {
		return valorPonderado;
	}
	public void setValorPonderado(String valorPonderado) {
		this.valorPonderado = valorPonderado;
	}
	
	
	

}
