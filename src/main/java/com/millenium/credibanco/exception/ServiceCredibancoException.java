package com.millenium.credibanco.exception;

public class ServiceCredibancoException extends Exception {

	public ServiceCredibancoException() {
		super();
	}

	public ServiceCredibancoException(String message, Throwable cause) {
		super(message, cause);
	}

	public ServiceCredibancoException(String message) {
		super(message);
	}

	public ServiceCredibancoException(Throwable cause) {
		super(cause);
	}

	
}
